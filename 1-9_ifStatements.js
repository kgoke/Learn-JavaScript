// if statements
var num = 8;
if(num > 0){
    console.log('number is positive\n');
}

// if else statements
if (num > 0) {
    console.log('number is positive\n');
} else {
    console.log('number is negative or 0\n')
}

// els if statements
if (num > 0) {
    console.log('number is positive\n');
} else if (num == 0) {
    console.log('number is 0\n')
} else {
    console.log('number is negative\n')
}
