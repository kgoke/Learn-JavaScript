// regular equality operators
/*

== equal to

=== equal value and equal type

!= not equal

!== not equal value and not equal type

> greater than

< less than

>= greater than or equal to

<= less than or equal to

? ternary operator
*/

// EXAMPLES:

1 == 1; // true

1 == '1'; // true

1 === 1; // true

1 === '1'; // false

1 != 1; // false

1 !== '1'; // true

2 > 1; // true

2 < 1; // false

// etc...

// LOGICAL OPERATORS
 if(1 && 2); // logical and

 if(1 || 2); // logical or

